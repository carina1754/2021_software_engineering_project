package brand;

public class Brand {
	private String brand_id="";
	private String brand_name="";
	private String brand_phone="";
	private String brand_info="";
	
	public String get_brand_id() {
		return brand_id;
	}
	public void set_brand_id(String brand_id) {
		this.brand_id = brand_id;
	}
	public String get_brand_name() {
		return brand_name;
	}
	public void set_brand_name(String brand_name) {
		this.brand_name = brand_name;
	}
	public String get_brand_phone() {
		return brand_phone;
	}
	public void set_brand_phone(String brand_phone) {
		this.brand_phone = brand_phone;
	}
	public String get_brand_info() {
		return brand_info;
	}
	public void set_brand_info(String brand_info) {
		this.brand_info = brand_info;
	}
}
